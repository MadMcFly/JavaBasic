package com.sl.otherclass;

public class Demo1_Math {

	public static void main(String[] args) {
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println("------------");
		
		System.out.println(Math.abs(-21));
		System.out.println("------------");
		
		System.out.println(Math.ceil(12.4));
		System.out.println(Math.floor(12.4));
		System.out.println("------------");
		
		System.out.println(Math.max(10.2, 10.3));
		System.out.println(Math.min(10, 15));
		System.out.println("------------");
		
		System.out.println(Math.pow(2, 4));
		System.out.println("------------");
		
		System.out.println(Math.random()); 		//from 0.0 to 1.0 [,)
		System.out.println("------------");
		
		System.out.println(Math.round(12.4));	//float to int
		System.out.println(Math.round(12.8));	//double to long
		System.out.println("------------");
		
		System.out.println(Math.sqrt(20));
	}

}
